def Tambah(a,b):
    ''' Menghitung penjumlahan dari dua bilangan int a dan b '''
    return a+b

def Kurang(a,b):
    ''' Menghitung pengurangan dari dua bilangan int a dan b '''
    return a-b

def Kali(a,b):
    ''' Menghitung perkalian dari dua bilangan int a dan b '''
    return a*b

def Bagi(a,b):
    ''' Menghitung pembagian dari dua bilangan int a dan b '''
    return a/b

def LuasPersegi(sisi):
    ''' Menghitung luas persegi '''
    return sisi*sisi

def LuasSegitiga(alas,tinggi):
    ''' Menghitung luas segitiga '''
    return 0.5*alas*tinggi